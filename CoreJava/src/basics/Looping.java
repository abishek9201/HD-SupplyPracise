package basics;

public class Looping {

	public static void main(String[] args) {
		for(int i=1;i<11;i++) {
			System.out.println(i);
		}
		System.out.println("----------------------------------");
		int a=1;
		while(a<11) {
		System.out.println(a);
		a++;
		}
		System.out.println("----------------------------------");
		int b=0;
		do {
			System.out.println(b);
			b++;
		}while(b<0);

	}
	}
