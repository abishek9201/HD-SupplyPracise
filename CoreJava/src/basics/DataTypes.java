package basics;

public class DataTypes {

	public static void main(String[] args) {
		/*
		 * Data Types in java
		 * Primitive data types 
		 * byte-1
		 * short-2
		 * int-4
		 * long-8
		 * double-8
		 * float-4 we can't directly assign a floating point value to float we convert a value to float 
		 * boolean true,false
		 * char single character
		 * This is the reason java is not 100% object oriented programming language
		 * */
		byte by=127;
		int a=10;
		int a2=-10;
		float b=10;
		float b2=12.5f;
		double c=20.5;
		boolean d=true;
		boolean d2=false;
		char e='e';
		System.out.println("---------------------------------");
		System.out.println(a);
		System.out.println(a2);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(d2);
		System.out.println(e);
		System.out.println(by);
		System.out.println(b2);
		

	}

}
